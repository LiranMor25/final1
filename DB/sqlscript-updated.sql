drop view `heroku_0791532c7c3fdff`.ans1;
create view `heroku_0791532c7c3fdff`.ans1 as
SELECT c.PinCode,p.Device,p.RiskLevel,p.TaskLevel,c.ClickType,c.answer as IsClicked 
FROM heroku_0791532c7c3fdff.clicks c join heroku_0791532c7c3fdff.participants p on c.PinCode=p.PinCode
 where ClickType like 'low_' or ClickType like 'high_'  ;

drop view `heroku_0791532c7c3fdff`.ans2;
create view `heroku_0791532c7c3fdff`.ans2 as
SELECT c.PinCode,c.answer ,(case when c.answer = a.answer then 1 else 0 end) as iscorrect
FROM heroku_0791532c7c3fdff.clicks c join heroku_0791532c7c3fdff.answers a on a.question=c.ClickType;

drop view `heroku_0791532c7c3fdff`.ans3;
create view `heroku_0791532c7c3fdff`.ans3 as
select compare.PinCode,avg(compare.iscorrect)*100 as grade
from `heroku_0791532c7c3fdff`.ans2  as compare
group by compare.PinCode;

/*the final query*/
select b.PinCode,b.Device,b.RiskLevel,b.TaskLevel,b.ClickType as Link,b.answer as isclicked,CAST(a.grade as decimal(10,2)) as Grade,q.*
from `heroku_0791532c7c3fdff`.ans3 a join `heroku_0791532c7c3fdff`.ans1 b on a.PinCode = b.PinCode join `heroku_0791532c7c3fdff`.Questionnaries q on q.PinCode = a.PinCode; 



